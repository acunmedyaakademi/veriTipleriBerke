console.log(number);

// Boolean: true veya false sorgularını temsil eder.
const boolean = true;
console.log(boolean);

// Null: Boş değeri temsiş eder.
const bosDeger = null;
console.log(bosDeger);

// Undefined: Tanımlanmamış değeri temsil eder.
